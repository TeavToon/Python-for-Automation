import os
import time

print("========== 📘 Time & Scheduling Cheatsheet ==========")

# ==========================================
# หมวดที่ 1: การหน่วงเวลาพื้นฐาน (Basic Sleep)
# ==========================================
print("\n--- 1. การหน่วงเวลาพื้นฐาน ---")
print('เริ่มทำงานได้')

# time.sleep(วินาที) -> สั่งให้โปรแกรมหยุดพักตามเวลาที่กำหนด
time.sleep(3) 
print('ผ่านไป 3 วินาทีแล้ว ตื่นได้แล้ว!')


# ==========================================
# หมวดที่ 2: การทำงานซ้ำแบบมีระยะพัก (Loop with Sleep)
# ==========================================
print("\n--- 2. การทำงานซ้ำแบบมีระยะพัก (For Loop) ---")
# ใช้สำหรับการทำอะไรซ้ำๆ ตามจำนวนรอบที่แน่นอน (เช่น วนลูปดาวน์โหลดไฟล์ 5 ไฟล์)
for i in range(1, 6):
    print(f"รอบที่ {i}: กำลังตรวจสอบระบบ...")
    time.sleep(2)  # พัก 2 วินาทีก่อนเริ่มรอบถัดไป
    
print("✅ ตรวจสอบเสร็จสิ้น! หุ่นยนต์ขอตัวไปนอน")


# ==========================================
# หมวดที่ 3: การสร้างบอทเฝ้าระวัง (Monitoring Bot)
# ==========================================
print("\n--- 3. การสร้างบอทเฝ้าระวังด้วย While Loop ---")
# สร้างโฟลเดอร์จำลองไว้ก่อน ป้องกันการ Error
sandbox_dir = 'My_Sandbox'
if not os.path.exists(sandbox_dir):
    os.mkdir(sandbox_dir)

target_file = os.path.join(sandbox_dir, 'secret.txt')
print(f"👀 เริ่มการเฝ้าระวังไฟล์: {target_file}")
print("💡 (คุณสามารถลองไปสร้างไฟล์ secret.txt ในโฟลเดอร์ My_Sandbox เพื่อหยุดการทำงานได้เลย)")

# while True: คือลูปอนันต์ (Infinity Loop) บอทจะทำงานไปเรื่อยๆ ไม่มีวันหยุดจนกว่าจะเจอคำสั่ง break
while True:
    print("กำลังเฝ้าระวัง...")
    time.sleep(3)  # หน่วงเวลา 3 วินาที เพื่อไม่ให้คอมพิวเตอร์ทำงานหนักเกินไป (CPU วิ่ง 100%)
    
    # เช็คว่ามีไฟล์เป้าหมายปรากฏขึ้นมาหรือยัง
    if os.path.exists(target_file):
        print("🚨 แจ้งเตือน! พบไฟล์ลับแล้ว! หยุดการทำงาน")
        break # สั่งทุบกระปุก! พังลูปทิ้งและหยุดการทำงานทันที

print("\n========== 🎉 จบการทำงานของบอท ==========")