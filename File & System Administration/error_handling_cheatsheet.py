print("\n--- เริ่มทดสอบการดักจับ Error ---")

try:
    # ลองเปิดไฟล์ที่ไม่มีอยู่จริง
    with open('ghost_file.txt', 'r', encoding='utf-8') as file:
        print(file.read())

# แผนสำรองที่ 1: ดักจับแบบเจาะจง (รู้ตัวว่าพังเพราะอะไร)
except FileNotFoundError:
    print("🚨 หัวหน้าครับ! บอทหาไฟล์ 'ghost_file.txt' ไม่เจอครับ แต่บอทยังทำงานต่อนะ!")

# แผนสำรองที่ 2: ดักจับแบบครอบจักรวาล (เผื่อเจอ Error แปลกๆ ที่เราไม่ได้เตรียมใจไว้)
except Exception as e:
    print(f"❌ บอทเจอข้อผิดพลาดที่ไม่คาดคิดครับ: {e}")


