print("========== 📘 Basic Text File I/O Cheatsheet ==========")

text_file = 'My_Note.txt'

# ==========================================
# หมวดที่ 1: การสร้างและเขียนไฟล์ข้อความ (Write)
# ==========================================
print("\n--- 1. การเขียนไฟล์ข้อความ (.txt) ---")

# โหมด 'w' (Write) คือการเปิดไฟล์เพื่อเขียน (ถ้ามีไฟล์เก่าอยู่ ข้อมูลเก่าจะถูกลบทิ้งและเขียนใหม่ทับลงไป)
# encoding='utf-8' สำคัญมาก! เป็นการบอกคอมพิวเตอร์ให้รองรับภาษาไทย ป้องกันปัญหาตัวอักษรกลายเป็นภาษาต่างด้าว (????)
with open(text_file, 'w', encoding='utf-8') as file:
    file.write("สวัสดี! นี่คือข้อความจากบอท Automation\n")  # \n คือการขึ้นบรรทัดใหม่
    file.write("ฉันสามารถอ่านไฟล์ txt ได้สบายมาก!")
    
print(f"✅ สร้างและเขียนข้อมูลลงไฟล์ '{text_file}' สำเร็จ!")


# ==========================================
# หมวดที่ 2: การอ่านไฟล์ข้อความ (Read)
# ==========================================
print("\n--- 2. การอ่านไฟล์ข้อความ (.txt) ---")

# โหมด 'r' (Read) คือการเปิดเพื่ออ่านอย่างเดียว (ปลอดภัยที่สุด เพราะเราจะไม่เผลอไปแก้ข้อมูลข้างใน)
with open(text_file, 'r', encoding='utf-8') as file:
    content = file.read()  # .read() จะดึงข้อความทั้งหมดในไฟล์ออกมาเป็นก้อนเดียว
    
    print("เนื้อหาที่อ่านได้จากไฟล์:")
    print("-" * 40)
    print(content)
    print("-" * 40)

print("\n========== 🎉 เสร็จสิ้นการทำงาน ==========")